char	*ft_strcat(char *dest, char *src)
{
	char	*p;

	p = dest;
	while (*p != '\0')
		p++;
	while (*src != '\0')
	{
		*p = *src;
		src++;
		p++;
	}
	*p = '\0';
	return (dest);
}
