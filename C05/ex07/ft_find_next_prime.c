int	ft_find_next_prime(int nb)
{
	int	n;

	if (nb < 2)
		return (2);
	n = 2;
	while (n <= 46340 && n * n <= nb)
	{
		if (nb % n == 0)
			nb = ft_find_next_prime(nb + 1);
		n++;
	}
	return (nb);
}
