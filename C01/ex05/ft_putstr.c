#include <unistd.h>

void	ft_putstr(char *str)
{
	int	str_len;
	int	i;

	str_len = 0;
	i = 0;
	while (str[i] != '\0')
	{
		str_len++;
		i++;
	}
	write(1, str, str_len);
}
